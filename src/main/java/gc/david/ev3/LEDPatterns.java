package gc.david.ev3;

public class LEDPatterns {

    /*
     * Button.LEDPattern(pattern)<br><br>
     *
     * 0: turn off button lights<br>
     * 1/2/3: static light green/red/yellow<br>
     * 4/5/6: normal blinking light green/red/yellow<br>
     * 7/8/9: fast blinking light green/red/yellow<br>
     * >9: same as 9.<br>
     */

    public static int OFF_BUTTON_LIGHTS = 0;

    public static int STATIC_LIGHT_GREEN = 1;

    public static int STATIC_LIGHT_RED = 2;

    public static int STATIC_LIGHT_YELLOW = 3;

    public static int NORMAL_BLINKING_LIGHT_GREEN = 4;

    public static int NORMAL_BLINKING_LIGHT_RED = 5;

    public static int NORMAL_BLINKING_LIGHT_YELLOW = 6;

    public static int FAST_BLINKING_LIGHT_GREEN = 7;

    public static int FAST_BLINKING_LIGHT_RED = 8;

    public static int FAST_BLINKING_LIGHT_YELLOW = 9;

}
